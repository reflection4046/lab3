package lab4;

public class Student 

{
   int ID;
   String Name;
   
public int getID() 
{
	return ID;
}

public void setID(int iD) 

{
	ID = iD;
}

public String getName() 

{
	return Name;
}

public void setName(String name)

{
	Name = name;
}

public String toString() 
{
	return "Student [ID=" + ID + ", Name=" + Name + "]";
}


}
